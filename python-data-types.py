## Integers, Floats, Strings, Booleans, Lists, Tuples, Sets, Dictionaries
# Integers = int = whole numbers
# Floats = float = decimal numbers
# Strings = str = "hello"
# Booleans = bool = True or False
# Lists = list = [1,2,3,4,5]
# Tuples = tuple (immutable so you can't change the values)= (1,2,3,4,5)
# Sets = set (unordered and no duplicates) = {1,2,3,4,5}
# Dictionaries = dict (key value pairs) = {"name": "John", "age": 30}

